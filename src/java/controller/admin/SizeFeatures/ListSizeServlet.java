    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String keyword = request.getParameter("keyword");
        SizeDAO sizeDAO = new SizeDAO();

        if (keyword == null) {
            Map<Size, Integer> categorys = sizeDAO.getAllSize();
            request.setAttribute("listSize", categorys);
        } else {
            Map<Size, Integer> cateSearch = sizeDAO.searchSizeByValue(keyword.trim());
            request.setAttribute("listSize", cateSearch);
        }

        request.getRequestDispatcher("/views/admin/ListSize.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String value = request.getParameter("size_value");
        
        SizeDAO sizeDAO = new SizeDAO();
        sizeDAO.addSize(Integer.valueOf(value));
        
        response.sendRedirect(request.getContextPath() + "/admin/sizes");
    }

