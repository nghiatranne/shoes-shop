    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        String keyword = request.getParameter("keyword");
        BrandDAO brandDAO = new BrandDAO();

        if (keyword == null) {
            Map<Brand, Integer> brands = brandDAO.getAllBrand();
            request.setAttribute("listBrand", brands);
        } else {
            Map<Brand, Integer> cateSearch = brandDAO.searchBrandByName(keyword.trim());
            request.setAttribute("listBrand", cateSearch);
        }

        request.getRequestDispatcher("/views/admin/ListBrand.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("brand_name");
        
        BrandDAO brandDAO = new BrandDAO();

        Brand b = new Brand(0, name, null, null, null);
        brandDAO.addBrand(b);
        
        response.sendRedirect(request.getContextPath() + "/admin/brands");
    }

