    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setAttribute("nav_category_manage", true);
        
        String keyword = request.getParameter("keyword");
        CategoryDAO da = new CategoryDAO();

        if (keyword == null) {
            Map<Category, Integer> categorys = da.getAllCategory();
            request.setAttribute("listCate", categorys);
        } else {
            Map<Category, Integer> cateSearch = da.searchCAtegoryByName(keyword.trim());
            request.setAttribute("listCate", cateSearch);
        }

        request.getRequestDispatcher("/views/admin/ListCategory.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("category_name");
        
        CategoryDAO da = new CategoryDAO();

        Category c = new Category(0, name, null, null, null, null);
        da.addCategory(c);
        
        response.sendRedirect(request.getContextPath() + "/admin/categories");
    }

